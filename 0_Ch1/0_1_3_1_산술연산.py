# 1.3.1 산술연산 p.28 
# python 에서는 산술연산이 가능합니다. 

print("1+2:",1+2) # "1+2:3" 출력
print("4*5:",4*5) # "4*5:20" 출력
print("7/5:",7/5) # "7/5:1.4" 출력
print("3**2:",3**2) # "3**2:9" 출력

# 1.3.2 자료형 p.29
# python 에서는 자료형을 지정하지 않아도 됩니다.
# 자료형을 지정하지 않아도 자동으로 인식합니다.
# 해당 자료형을 확인 할 수 있습니다. 

print("10 class is",type(10)) # <class 'int'> 출력
print("2.718 class is",type(2.718)) # <class 'float'> 출력
print(" \"Hello\" class is",type("Hello")) # <class 'str'> 출력

